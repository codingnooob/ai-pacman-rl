class MazeGenerator:
    def __init__(self, width=28, height=31):
        self.width = width
        self.height = height
        
    def generate(self):
        """Use classic Pacman maze layout"""
        # Classic Pacman maze pattern (28x31)
        pattern = [
            "############################",
            "#............##............#",
            "#.####.#####.##.#####.####.#",
            "#o####.#####.##.#####.####o#",
            "#.####.#####.##.#####.####.#",
            "#..........................#",
            "#.####.##.########.##.####.#",
            "#.####.##.########.##.####.#",
            "#......##....##....##......#",
            "######.##### ## #####.######",
            "######.##### ## #####.######",
            "######.##          ##.######",
            "######.## ###--### ##.######",
            "######.## #      # ##.######",
            "      .   #      #   .      ",
            "######.## #      # ##.######",
            "######.## ######## ##.######",
            "######.##          ##.######",
            "######.## ######## ##.######",
            "######.## ######## ##.######",
            "#............##............#",
            "#.####.#####.##.#####.####.#",
            "#.####.#####.##.#####.####.#",
            "#o..##.......  .......##..o#",
            "###.##.##.########.##.##.###",
            "###.##.##.########.##.##.###",
            "#......##....##....##......#",
            "#.##########.##.##########.#",
            "#.##########.##.##########.#",
            "#..........................#",
            "############################"
        ]
        
        # Convert to maze array
        maze = []
        for row in pattern:
            maze_row = []
            for char in row:
                if char == '#':
                    maze_row.append(1)  # Wall
                else:
                    maze_row.append(0)  # Path
            maze.append(maze_row)
        
        return maze
